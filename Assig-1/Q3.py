# Practice of Assignment Operators in Python

# Initial value
a = 10
print("Initial value of a =", a)

# += Operator
a += 5
print("After a += 5  ->", a)

# -= Operator
a -= 3
print("After a -= 3  ->", a)

# *= Operator
a *= 2
print("After a *= 2  ->", a)

# /= Operator
a /= 4
print("After a /= 4  ->", a)

# %= Operator
a %= 3
print("After a %= 3  ->", a)

# **= Operator
a **= 2
print("After a **= 2 ->", a)

# //= Operator
a //= 2
print("After a //= 2 ->", a)